    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (new java.sql.Date(jDateChooser2.getDate().getTime()).compareTo(new java.sql.Date(jDateChooser1.getDate().getTime())) < 0 
                || new java.sql.Date(jDateChooser2.getDate().getTime()).compareTo(new java.sql.Date(jDateChooser1.getDate().getTime())) == 0) {
            JOptionPane.showMessageDialog(null, "Fecha mal ingresada", "Arrendar vehículo", JOptionPane.ERROR_MESSAGE);
        }
        else {
            if (modelo.getValueAt(jTable2.getSelectedRow(), 0) == null) {
                JOptionPane.showMessageDialog(null, "No existe un vehículo", "Arrendar Vehículo", JOptionPane.INFORMATION_MESSAGE);
            }
            else {
                int n = JOptionPane.showConfirmDialog(null, "¿Desea rentar el vehículo "
                    +modelo.getValueAt(jTable2.getSelectedRow(), 1)+" ?", "Confirmar Vehículo",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (n == 0) {
                    JOptionPane.showMessageDialog(null, "Datos del Pedido:\nNombre: "
                    +jTextField1.getText()+"\nVehículo: "+modelo.getValueAt(jTable2.getSelectedRow(), 1)
                    +"\nNúmero de Pedido: "+guardarCliente(), "Confirmar Vehículo", JOptionPane.INFORMATION_MESSAGE);
                    cargarVehiculos();
                }
            }
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

