    private void cargarVehiculos() {
        String sql = "SELECT * FROM vehiculo WHERE Disponibilidad = 'Disponible'";
        
        try {
            PreparedStatement pst = VentanaLogin.con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            modelo.setRowCount(0);
            while (rs.next()) {                
                modelo.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), 
                rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8),
                rs.getString(9), rs.getString(10), rs.getString(11), rs.getString(12), rs.getString(13),
                rs.getString(14), rs.getString(15)});
            }
            modelo.setRowCount(11);
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(PanelArrendar.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private int guardarCliente() {
        int n = 0;
        try {
            String sql = "INSERT INTO cliente (Nombre, Edad, Deuda, FechaDevolucion, ID_Vehiculo)"
            + " VALUES ('"+jTextField1.getText()+"', "+Integer.valueOf((String) jComboBox1.getSelectedItem())
            +", "+modelo.getValueAt(jTable2.getSelectedRow(), 2)+", '"
            +new java.sql.Date(jDateChooser2.getDate().getTime())+"', "+modelo.getValueAt(jTable2.getSelectedRow(), 0)+")";
            
            PreparedStatement pst = VentanaLogin.con.prepareStatement(sql);
            pst.execute();
            
            String sql2 = "UPDATE vehiculo SET Disponibilidad = 'No disponible' "
                    + "WHERE ID = "+modelo.getValueAt(jTable2.getSelectedRow(), 0);
            
            PreparedStatement pst2 = VentanaLogin.con.prepareStatement(sql2);
            pst2.execute();
            
            String sql3 = "SELECT MAX(ID) FROM cliente";
            
            PreparedStatement pst3 = VentanaLogin.con.prepareStatement(sql3);
            ResultSet rs = pst3.executeQuery();
            while (rs.next()) {                
                n = rs.getInt(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(PanelArrendar.class.getName()).log(Level.SEVERE, null, ex);
        }
        return n;
    }
    
